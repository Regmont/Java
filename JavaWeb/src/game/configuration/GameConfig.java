package game.configuration;

/**
 * Центральный класс конфигурации игровых параметров.
 * <p>
 * Содержит константы для настройки производительности, управления камерой
 * и обработки ввода. Все поля являются публичными и неизменяемыми.
 *
 * @author Дунин Михаил Сергеевич
 * @version 1.0
 */
public class GameConfig {
    /**
     * Целевое количество кадров в секунду.
     * <p>
     * Используется для расчета времени кадра и регулировки игрового цикла.
     * Значение 60 соответствует стандартной частоте обновления.
     */
    public static final int TARGET_FPS = 60;

    /**
     * Максимальный угол наклона камеры по вертикали (в радианах).
     * <p>
     * Ограничивает подъем камеры вверх, предотвращая полный переворот.
     */
    public static final double MAX_PITCH = Math.PI / 2.0 - 0.01; // (89.99°)

    /**
     * Минимальный угол наклона камеры по вертикали (в радианах).
     * <p>
     * Ограничивает опускание камеры вниз, предотвращая полный переворот.
     */
    public static final double MIN_PITCH = -Math.PI / 2.0 + 0.01; // (-89.99°)

    /**
     * Коэффициент чувствительности мыши.
     * <p>
     * Определяет, насколько сильно движение мыши влияет на поворот камеры.
     * Умножается на смещение курсора в пикселях.
     */
    public static final double MOUSE_SENSITIVITY = 0.003;

    /**
     * Коэффициент сглаживания движения мыши.
     * <p>
     * Используется для уменьшения дельты движения между кадрами по экспоненциальному закону.
     * Значение должно быть в диапазоне (0, 1).
     */
    public static final double MOUSE_SMOOTHING_FACTOR = 2.0 / 3.0;

    /**
     * Максимальная длительность кадра в миллисекундах.
     * <p>
     * Предотвращает "спираль смерти" при проседании производительности.
     * Если кадр длится дольше, время обрезается до этого значения.
     */
    public static final long MAX_FRAME_TIME_MS = 250;
}
